                    theo nội dung trên. </div>
<div class="bocuc head"> &nbsp; &nbsp; 2. Đổi hàng lỗi do quá trình vận chuyển. </div>
<div class="bocuc"> &nbsp; &nbsp; - Khi nhận được sản phẩm bị hư hại do quá trình vận chuyển, quý khách cần phản hồi lại 
                    ngay với MAPH. Trong vòng 48 giờ quý khách vui lòng liên hệ với chúng tôi để được hỗ trợ. 
                    Trong trường hợp đã có biên bản đồng kiểm giữa nhà vận chuyển và người mua, MAPH sẽ không 
                    hỗ trợ đổi hàng trong mọi điều kiện. </div>
<div class="bocuc"> &nbsp; &nbsp; - Vì MAPH áp dụng phương thức COD (được kiểm tra hàng trước khi nhận) nên 
                    chúng tôi khuyến khích quý khách kiểm tra hàng trước khi nhận. </div>
<div class="bocuc head"> &nbsp; &nbsp; 3. Bảo hành sản phẩm do lỗi nhà cung cấp. </div>
<div class="bocuc"> &nbsp; &nbsp; - Chúng tôi áp dụng hình thức bảo hành cho sản phẩm đạt các tiêu chí sau: </div>
