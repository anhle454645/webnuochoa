<div class="bocuc"> &nbsp; &nbsp; &nbsp; + Sản phẩm còn thời gian bảo hành theo quy định. </div>
<div class="bocuc"> &nbsp; &nbsp; &nbsp; + Sản phẩm lỗi kỹ thuật do nhà sản xuất. </div>
<div class="bocuc"> &nbsp; &nbsp; - “KHÔNG” bảo hành các lỗi hỏng hóc do người sử dụng: làm rơi, làm vỡ, sử dụng sai 
                    hướng dẫn và quy cách. </div>
<div class="bocuc head"> &nbsp; &nbsp; 4. Yêu cầu sản phẩm khi đổi trả. </div>
<div class="bocuc"> &nbsp; &nbsp; - Sản phẩm không có dấu hiệu đã qua sử dụng, còn nguyên tem túi đầy đủ (nếu có). </div>
<div class="bocuc"> &nbsp; &nbsp; - Sản phẩm còn đầy đủ phụ kiện, tem cùng quà tặng kèm theo (nếu có). </div>
